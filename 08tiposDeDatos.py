#* tipos de datos entero o largo
numero=15
print(numero,type(numero))

#* tipo de dato float
numerofloat=15.5
print(numerofloat,type(numerofloat))

#* tipo de dato numero complejo
numeroComplejo=5 +6j
print(numeroComplejo, type(numeroComplejo))

#* tipo de dato string
nombre="brandon"
print(nombre, type(nombre))

#*tipo de dato booleano
verdadero_Falso =3==3
print(verdadero_Falso,type(verdadero_Falso))
